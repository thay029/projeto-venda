    public void preencheTabela(){
        notaDAO nDAO = new notaDAO();
        List<NotaFiscal> listaNotas = nDAO.getNotas();
        
        DefaultTableModel tabelaNotas = (DefaultTableModel) tblNotas.getModel();
        
        for(NotaFiscal n: listaNotas){
            Object[] obj = new Object[]{
                n.getIdNota(),
                n.getData(),
                n.getTotal(),
                n.getNomeCliente()
            };
            tabelaNotas.addRow(obj);
        }
    }

